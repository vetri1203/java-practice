public class SwithcCondition {
    public static void main(String[] args)
    {
        String month = "Jan";

        switch(month){
            case "Jan":
                System.out.println("This is Jan month");
                break;
            case "Feb":
                System.out.println("This is Feb month");
            default:
            System.out.println("Unkown month");
        }
    }
}
